# bool 자료형

"""
bool 자료형은 오직 True, False 만을 가지며 참, 거짓을 나타내는 자료형이다
"""

# 자료형의 참과 거짓

print(bool("python"))  # true
print(bool(""))  # false
print(bool([1, 2]))  # true
print(bool([]))  # false
print(bool(()))  # false
print(bool({}))  # false
print(bool(0))  # false
print(bool(1))  # true
print(bool(None))  # false
